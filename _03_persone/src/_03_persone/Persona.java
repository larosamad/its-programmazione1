package _03_persone;

	public class Persona{
		
		// attributi
		public String name;
		public String surname;
		public int age;
		public String password;
		
		// costruttore
		public Persona(String name, String surname, int age, String password) {
			this.name = name;
			this.surname = surname;
			this.age = age;
			this.password = password;
		}
	}